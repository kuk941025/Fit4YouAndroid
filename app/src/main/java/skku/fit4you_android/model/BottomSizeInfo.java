package skku.fit4you_android.model;

public class BottomSizeInfo extends ClothingSizeInfo {
    private int down_length, thigh, rise, hem, waist;

    public BottomSizeInfo() {
    }

    public int getDown_length() {
        return down_length;
    }

    public void setDown_length(int down_length) {
        this.down_length = down_length;
    }

    public int getThigh() {
        return thigh;
    }

    public void setThigh(int thigh) {
        this.thigh = thigh;
    }

    public int getRise() {
        return rise;
    }

    public void setRise(int rise) {
        this.rise = rise;
    }

    public int getHem() {
        return hem;
    }

    public void setHem(int hem) {
        this.hem = hem;
    }

    public int getWaist() {
        return waist;
    }

    public void setWaist(int waist) {
        this.waist = waist;
    }
}
