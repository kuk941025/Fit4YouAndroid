package skku.fit4you_android.model;

//All values are in cm
public class BodySize {
    private int head_width, head_height;
    private int arm_width, arm_height;
    private int body_width, body_height;
    private int leg_width, leg_height;

    public BodySize() {
    }

    public int getHead_width() {
        return head_width;
    }

    public void setHead_width(int head_width) {
        this.head_width = head_width;
    }

    public int getHead_height() {
        return head_height;
    }

    public void setHead_height(int head_height) {
        this.head_height = head_height;
    }

    public int getArm_width() {
        return arm_width;
    }

    public void setArm_width(int arm_width) {
        this.arm_width = arm_width;
    }

    public int getArm_height() {
        return arm_height;
    }

    public void setArm_height(int arm_height) {
        this.arm_height = arm_height;
    }

    public int getBody_width() {
        return body_width;
    }

    public void setBody_width(int body_width) {
        this.body_width = body_width;
    }

    public int getBody_height() {
        return body_height;
    }

    public void setBody_height(int body_height) {
        this.body_height = body_height;
    }

    public int getLeg_width() {
        return leg_width;
    }

    public void setLeg_width(int leg_width) {
        this.leg_width = leg_width;
    }

    public int getLeg_height() {
        return leg_height;
    }

    public void setLeg_height(int leg_height) {
        this.leg_height = leg_height;
    }
}
